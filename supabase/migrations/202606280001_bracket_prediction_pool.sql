create table public.tournament_matches (
  id bigint primary key,
  code text not null unique,
  round public.round not null,
  slot_order integer not null,
  match_date timestamptz not null,
  venue_name text not null,
  home_team_id bigint references public.teams(id) on delete restrict,
  away_team_id bigint references public.teams(id) on delete restrict,
  home_source_match_id bigint references public.tournament_matches(id) on delete restrict,
  home_source_type public.source_type,
  away_source_match_id bigint references public.tournament_matches(id) on delete restrict,
  away_source_type public.source_type,
  check (
    (round = 'r32' and home_team_id is not null and away_team_id is not null)
    or (round <> 'r32' and home_source_match_id is not null and away_source_match_id is not null)
  ),
  check ((home_source_match_id is null) = (home_source_type is null)),
  check ((away_source_match_id is null) = (away_source_type is null))
