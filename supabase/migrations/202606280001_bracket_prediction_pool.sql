create table public.official_results (
  match_id bigint primary key references public.tournament_matches(id) on delete cascade,
  home_score integer not null check (home_score >= 0),
  away_score integer not null check (away_score >= 0),
  advancing_team_id bigint references public.teams(id) on delete set null,
